package de.gematik.ti.erp.app.utils.extensions

import android.view.View
import androidx.compose.foundation.layout.WindowInsets
import androidx.compose.foundation.layout.ime
import androidx.compose.runtime.Composable
import androidx.compose.runtime.State
import androidx.compose.runtime.rememberUpdatedState
import androidx.compose.ui.platform.LocalDensity
import androidx.compose.ui.platform.SoftwareKeyboardController
import androidx.core.view.WindowInsetsCompat
import kotlinx.coroutines.delay

const val KEYBOARD_WAIT_TIME = 500L

val View.isKeyboardVisible: Boolean
    get() = WindowInsetsCompat
        .toWindowInsetsCompat(rootWindowInsets)
        .isVisible(WindowInsetsCompat.Type.ime())

/**
 * @return State<Boolean> true if the keyboard is visible
 */
@Composable
fun keyboardAsState(): State<Boolean> {
    val isImeVisible = WindowInsets.ime.getBottom(LocalDensity.current) > 0
    return rememberUpdatedState(isImeVisible)
}

suspend fun SoftwareKeyboardController.showKeyboardOnNotOpen(isKeyboardOpen: Boolean) {
    delay(KEYBOARD_WAIT_TIME)
    if (!isKeyboardOpen) {
        show()
    }
}
